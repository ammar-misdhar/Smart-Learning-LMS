    public void inserToSQL() {
        String fName = txtFirstName.getText();
        String LName = txtLastName.getText();
        String contact = txtContact.getText();
        String password = txtPassword.getText();

        try {
            // create connection to database
            Connection con = database.db.getConnection();
            // Create prepared statement with the sql query
            PreparedStatement st1 = con.prepareStatement("UPDATE `smart_learning`.`register` SET `firstName` = ?, `lastName` = ?, `password` = ?, `contact` = ? WHERE (`id` = '" + idpas + "');");
            // execute query

            st1.setString(1, fName);
            st1.setString(2, LName);
            st1.setString(3, password);
            st1.setString(4, contact);

            st1.executeUpdate();

            // show success message
        } catch (SQLException ex) {
            Logger.getLogger(ChangeProfile.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
