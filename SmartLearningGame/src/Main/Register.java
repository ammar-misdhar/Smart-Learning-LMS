    public void sqlSubject(String uName) {

        String id = "";
        try {
            Connection con = database.db.getConnection();
            String sql = "SELECT id FROM smart_learning.register where userName=? ;";
            PreparedStatement st = con.prepareStatement(sql);
            st.setString(1, uName);
            ResultSet rs = st.executeQuery();
            while (rs.next()) {
                id = rs.getString("id");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            // create connection to database
            Connection con = database.db.getConnection();
            // Create prepared statement with the sql query
            PreparedStatement st1 = con.prepareStatement("INSERT INTO `smart_learning`.`science` (`stRegId`) VALUES (?);");
            PreparedStatement st2 = con.prepareStatement("INSERT INTO `smart_learning`.`ict` (`stRegId`) VALUES (?);");
            PreparedStatement st3 = con.prepareStatement("INSERT INTO `smart_learning`.`maths` (`stRegId`) VALUES (?);");

            st1.setString(1, id);
            st2.setString(1, id);
            st3.setString(1, id);
            // execute query
            st1.executeUpdate();
            st2.executeUpdate();
            st3.executeUpdate();

            // show success message
        } catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

