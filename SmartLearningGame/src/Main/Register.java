    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed

//get valuse to variable
        String fName = txtFirstName.getText();
        String lName = txtLastName.getText();
        String uName = txtUserName.getText();

        String contact = txtCotact.getText();
        if (contact.equals("Enter the contact")) {
            contact = "";
        }

        String password = txtPassword.getText();
        String conPass = txtConPassword.getText();

//chack epmty field
        if ((fName.equals("") || fName.equals("Enter the first name")) || (lName.equals("") || lName.equals("Enter the last name")) || (password.equals("") || password.equals("Enter the password")) || (conPass.equals("") || conPass.equals("Enter the password"))) {
            lblError.setText("Some fields are empty !");
        } else {
            lblError.setText("");

//chack password and conPass            
            if (password.equals(conPass)) {

//chack user name is used or not
                try {
                    Connection con = database.db.getConnection();
                    String sql = "SELECT userName FROM smart_learning.register WHERE userName=?;";
                    PreparedStatement st = con.prepareStatement(sql);
                    st.setString(1, uName);
                    ResultSet rs = st.executeQuery();
                    if (rs.next()) {

                        lblError.setText(uName + " is alrady excisted !");
//Inset to database                        
                    } else {
                        // Create prepared statement with the sql query
                        PreparedStatement st1 = con.prepareStatement("INSERT INTO `smart_learning`.`register` (`firstName`, `lastName`, `userName`, `password`, `contact`,`Gender`) VALUES (? ,? ,? ,? ,? ,? );");
                        // execute query
                        st1.setString(1, fName);
                        st1.setString(2, lName);
                        st1.setString(3, uName);
                        st1.setString(4, password);
                        st1.setString(5, contact);
                        st1.setString(6, gender);

                        st1.executeUpdate();
                        
// insert id into subject database
                        sqlSubject(uName);
                        // show success message
                        lblError.setForeground(new Color(37, 105, 214));
                        lblError.setText("Succes fully created");

                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                lblError.setText("Password and Confirm Password is dosn't match");
            }
        }


    }//GEN-LAST:event_btnSignupActionPerformed

